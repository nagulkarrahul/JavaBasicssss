package com.company;

public class CWH_21_WhileLoops {
    public static void main(String[] args) {

        int i=1;
        while(i<=10){
            System.out.println(i);
            i++;
        }
        System.out.println("While loop running is finish");

        int i1=100;
        while(i1<=200){
            System.out.println(i1);
            i1++;
        }
        System.out.println("While loop running is finish");

    }
}
